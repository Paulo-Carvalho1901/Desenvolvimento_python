"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica e forte
str = string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Paulo')
print(1, "Paulo 'Carvalho'")

# Aspas duplas
print("Paulo")
print(2, 'Paulo "Carvalho"')

# Escape
print("Paulo \"Carvalho\"")

# r
print(r"Paulo \"Carvalho\"")