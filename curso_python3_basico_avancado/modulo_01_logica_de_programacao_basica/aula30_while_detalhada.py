"""
Repetições
while (enquanto)
Executa uma ação enquanto a condiçao for verdadeira
Loope infinito -> Quando um código não tem fim
"""

contador = 0

while contador <= 10: # condição
    contador = contador + 1 # incrementando o contador
    print(contador) # mostrando o contador

print('ACABOU!') # FORA DO WHILE
